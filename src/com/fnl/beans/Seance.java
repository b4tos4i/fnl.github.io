package com.fnl.beans;

public class Seance {
	private int sets;
	private int reps;
	private int weight;
	private int rpe;
	
	public int getSets() {
		return sets;
	}
	public void setSets(int sets) {
		this.sets = sets;
	}
	public int getReps() {
		return reps;
	}
	public void setReps(int reps) {
		this.reps = reps;
	}
	public int getWeight() {
		return weight;
	}
	public void setWeight(int weight) {
		this.weight = weight;
	}
	public int getRpe() {
		return rpe;
	}
	public void setRpe(int rpe) {
		this.rpe = rpe;
	}
	
	
}
